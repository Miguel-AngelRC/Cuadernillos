function valida_test1(preguntas, test, divresultado  ){
    let respuestas = validarRespuestas (preguntas,test,false);
    let mostrar = "";
    let mensaje ="";
    if (respuestas){
        let suma = 0;
        //sumar 
        for(i in respuestas )  suma += (parseInt(i)) * (parseInt(respuestas[i]));
       
        let sumaX2 = suma *2;
         mostrar = "<p> Porcentaje total %: "+suma+"</p>" +
                      "<p> Puntaje total multiplicado por 2: "+sumaX2+"</p>";
         mensaje = "Suma: ";
    }

    mostrarResultado(mostrar, divresultado, mensaje);
}



